public class contoh8{
    public static void main(String args[]){
        System.out.println("1. Contoh Pengulangan For"); 
        int i;

        for (i = 1;i<=10;i++){
            System.out.println("Sesuatu "+i);
        }
        System.out.println("");
        System.out.println("2. Contoh Pengulangan DoWhile");
        byte a;
        a = 1;
        do{
            System.out.println("yang ada");
            a++;
        }while(a <= 10);
        System.out.println("");
        System.out.println("3. Contoh Pengulangan While");
        byte q;
        q = 1;
        while(q <= 10){
            System.out.println("di hatimu");
            q++;
        }
    }
}