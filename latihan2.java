public class latihan2{
    public static void main(String args[]){
        int bilangan1 = 365, bilangan2 =12, hasil1,hasil2,hasil3,hasil4;
        hasil1 = bilangan1 + bilangan2;
        hasil2 = bilangan1 - bilangan2;
        hasil3 = bilangan1 * bilangan2;
        hasil4 = bilangan1 / bilangan2;

        System.out.println("Program Dasar Menghitung Operasi Aritmatika");
        System.out.println("");
        System.out.println("Diketahui : ");
        System.out.println("1. Bilangan Pertama : 365");
        System.out.println("2. Bilangan kedua   : 12");
        System.out.println("");
        System.out.println("Hasil Operasi Aritmatika");
        System.out.println("");
        System.out.println("1. Aritmatika Penjumlahan(+)");
        System.out.println("365 + 12 -> maka hasil penjumlahannya adalah : "+ hasil1);
        System.out.println("");
        System.out.println("2. Aritmatika Pengurangan(-)");
        System.out.println("365 - 12 -> maka hasil pengurangannya adalah : "+ hasil2);
        System.out.println("");
        System.out.println("3. Aritmatika Perkalian(x)");
        System.out.println("365 x 12 -> maka hasil perkaliannya adalah : "+ hasil3);
        System.out.println("");
        System.out.println("4. Aritmatika pembagian(:)");
        System.out.println("365 : 12 -> maka hasil pembagiannya adalah : "+ hasil4);
        System.out.println("");
        System.out.println("Sekian dan Terima Kasih ^_^");
    }
}